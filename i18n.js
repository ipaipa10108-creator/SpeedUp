let currentLang = localStorage.getItem('speedup-lang') || 'zh-TW';

function t(key) {
  return I18N[currentLang]?.[key] || I18N['zh-TW']?.[key] || key;
}

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('speedup-lang', lang);
  document.documentElement.lang = lang;
  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    el.textContent = t(key);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    const key = el.getAttribute('data-i18n-placeholder');
    el.placeholder = t(key);
  });
  document.querySelectorAll('[data-i18n-title]').forEach((el) => {
    const key = el.getAttribute('data-i18n-title');
    el.title = t(key);
  });
  const langBtn = document.getElementById('lang-toggle');
  if (langBtn) langBtn.textContent = t('lang');
  if (typeof window.renderBookmarks === 'function') {
    window.renderBookmarks();
  }
}

function toggleLang() {
  setLang(currentLang === 'zh-TW' ? 'en' : 'zh-TW');
}

function getCurrentLang() {
  return currentLang;
}
